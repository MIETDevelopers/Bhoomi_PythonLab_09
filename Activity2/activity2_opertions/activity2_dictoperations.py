#operations on dictionary

# creating a dictionary

dict = {1:"one", 2:"two", 3:"three"}
print("The dictionary is...")
print(dict)
print()

# inserting in a dictionary

dict[4] = "four"
print(dict)
print()

# updating a dictionary

dict.update({4:"FOUR"})
print(dict)
print()

# deleting from a dictionary

del dict[4]
print(dict)
print()

